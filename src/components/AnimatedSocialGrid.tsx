const containerVariants: Variants = {
  hidden: { opacity: 0 },
  visible: {
    opacity: 1,
    transition: {
      staggerChildren: 0.05,
    },
  },
};

const iconVariants: Variants = {
  hidden: { opacity: 0, y: 10 },
  visible: {
    opacity: 1,
    y: 0,
    transition: { type: 'spring', stiffness: 200, damping: 20 },
  },
};

const reducedVariants: Variants = {
  hidden: { opacity: 0 },
  visible: { opacity: 1, transition: { duration: 0.2 } },
};

export function AnimatedSocialGrid({ reduceMotion }: AnimatedSocialGridProps): React.ReactElement {
  const activeContainerVariants = reduceMotion ? reducedVariants : containerVariants;
  const activeIconVariants = reduceMotion ? reducedVariants : iconVariants;
  const { triggerHaptic } = useHaptic();

  const handleTap = (): void => {
    triggerHaptic('light');
  };

  return (
    <motion.nav
      aria-label="Social media links"
      className="flex justify-center gap-4 pb-safe"
      variants={activeContainerVariants}
      initial="hidden"
      animate="visible"
    >
      {socialLinks.map((link) => (
        <motion.a
          key={link.name}
          href={link.href}
          target="_blank"
          rel="noopener noreferrer"
          className="flex h-12 w-12 items-center justify-center rounded-full text-[color:var(--icon-color)] transition-colors hover:text-[color:var(--icon-color-hover)] outline-none focus-visible:ring-2 focus-visible:ring-[color:var(--ring-color)]"
          variants={activeIconVariants}
          whileHover={reduceMotion ? undefined : { y: -3 }}
          whileTap={reduceMotion ? undefined : { scale: 0.9, opacity: 0.7 }}
          onTap={handleTap}
          aria-label={link.name}
        >
          {link.icon}
        </motion.a>
      ))}
    </motion.nav>
  );
}
